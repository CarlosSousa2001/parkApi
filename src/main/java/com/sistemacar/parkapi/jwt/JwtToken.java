package com.sistemacar.parkapi.jwt;

public class JwtToken {

    private String token;

    private JwtToken() {
    }

    public JwtToken(String token) {
        this.token = token;
    }

    public String getToken() {
        return token;
    }

    public void setToken(String token) {
        this.token = token;
    }
}
